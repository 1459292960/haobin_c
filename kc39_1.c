#include<stdio.h>
int main(void)
{
    int i;
    scanf("%d\n",&i); // 第一个scanf加入\n换行符，这样enter键就不会当成输入输给第二个scanf
    scanf("m%d",&i); 
    /* m为非输入控制符，需要用户在输入时原样输入，否则不匹配将发生错误*/
    printf("i = %d\n",i); // i= 为飞入输出控制符，原样输出
    return 0;
}
