# include <stdio.h>

int main(void)
{
    int i=-5; // 其补码 1111 1111 1111 1111 1111 1111 1111 1011
    int j=10; // 其补码 0000 0000 0000 0000 0000 0000 0000 1010
    int k;
    k=i&j; // 按位与
    printf("%d\n",k);
    k=i&& j; // 逻辑与
    printf("%d\n",k);
    return 0;
}

